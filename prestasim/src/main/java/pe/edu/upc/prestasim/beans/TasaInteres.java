package pe.edu.upc.prestasim.beans;

public class TasaInteres {

	private int idtasa;
	private int identidad;
	private int idtipoprestamo;
	private int edadtope;
	private Double ingresomin;
	private int tasa;
	private int cuotainicial;

	public int getIdtasa() {
		return idtasa;
	}
	public void setIdtasa(int idtasa) {
		this.idtasa = idtasa;
	}
	public int getIdentidad() {
		return identidad;
	}
	public void setIdentidad(int identidad) {
		this.identidad = identidad;
	}
	public int getIdtipoprestamo() {
		return idtipoprestamo;
	}
	public void setIdtipoprestamo(int idtipoprestamo) {
		this.idtipoprestamo = idtipoprestamo;
	}
	public int getEdadtope() {
		return edadtope;
	}
	public void setEdadtope(int edadtope) {
		this.edadtope = edadtope;
	}
	public Double getIngresomin() {
		return ingresomin;
	}
	public void setIngresomin(Double ingresomin) {
		this.ingresomin = ingresomin;
	}
	public int getTasa() {
		return tasa;
	}
	public void setTasa(int tasa) {
		this.tasa = tasa;
	}
	public int getCuotainicial() {
		return cuotainicial;
	}
	public void setCuotainicial(int cuotainicial) {
		this.cuotainicial = cuotainicial;
	}

}
